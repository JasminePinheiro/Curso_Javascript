const escola = "MineSchool";
console.log(escola.charAt(4)); // irá mostrar a letra que encontra-se na quarta posição do vetor
console.log(escola.substring(4)); // vai começar a imprimir a string a partir do quarto index do vetor
console.log(escola.substring(0,4)); // vai imprimir a string começando do index 0 até o index 4
console.log("Escola ".concat(escola).concat("!!")); // concatenando as strings
console.log(escola.replace('S', 's')); // serve para substituir algum caracter da string 
console.log('Jasmine, Julia, Ana'.split(',')); // o comando split serve pra criar um array Unidimensional/vetor
console.log("O nome da escolar é: " + escola + ".");

